<script lang="ts">
import { useRouter } from 'vue-router';
import { defineComponent } from 'vue';
import { ROUTE_ERROR_404 } from '@/helpers/enums/routes';

export default defineComponent({
  name: 'LoanContent',
  setup: () => {
    const router = useRouter();

    const linkTo = (event: MouseEvent) => {
      const { href } = event.target as { href?: string };
      if (!href) return;

      const is404Page = router.resolve(href).name === ROUTE_ERROR_404;
      if (is404Page) return;

      event.preventDefault();
      void router.push(href);
    };

    return {
      linkTo,
    };
  },
});
</script>

